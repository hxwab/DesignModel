package com.factory.abstractfactory;

public class User {

	private int id;
	private String name;
	
	/**
	 * get function
	 * @return 
	 */
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	 
}
